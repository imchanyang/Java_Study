
public class Gugudan3 {
    public static void main(String[] args){
        System.out.println("----------while-------------");
        // 6단(반복문 - while)
        int i = 1;

        while(i < 10) {
            System.out.println(6 * i);
            i++;
        }

        System.out.println("----------for-------------");
        // 7단(반복문 - for)
        for(int j = 1; j < 10; j++) {
            System.out.println(7 * j);
        }
    }
}
